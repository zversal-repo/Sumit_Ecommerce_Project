package org.sumit.ECom.Model;

public class Link {

	private String link;
	private String rel;

	public void setLink(String link) {
		this.link = link;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getLink() {
		return link;
	}

	public String getRel() {
		return rel;
	}

}
